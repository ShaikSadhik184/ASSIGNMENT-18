# create three nested dictionaries
person1 = {
    "name": "John Smith",
    "age": 30,
    "gender": "male"
}

person2 = {
    "name": "Jane Doe",
    "age": 25,
    "gender": "female"
}

person3 = {
    "name": "Bob Johnson",
    "age": 40,
    "gender": "male"
}

# create a dictionary containing the three nested dictionaries
people = {
    "person1": person1,
    "person2": person2,
    "person3": person3
}

# print the outer dictionary
print(people)
